package java_basics;

public class Literals {

	public static void main(String[] args) {
		int octal = 032;
		System.out.println(octal);
		
		int binary = 0b1010101;
		System.out.println(binary);
		
		int hexa = 0x101;
		System.out.println(hexa);
		
		int hex = 0B1010;
		System.out.println(hex);
	}
}
